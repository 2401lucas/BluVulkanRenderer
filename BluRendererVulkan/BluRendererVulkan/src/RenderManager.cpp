namespace std {
	template<> struct hash<Core::Rendering::Vertex> {
		size_t operator()(Core::Rendering::Vertex const& vertex) const {
			return ((hash<glm::vec3>()(vertex.pos) ^ (hash<glm::vec3>()(vertex.color) << 1)) >> 1) ^ (hash<glm::vec2>()(vertex.texCoord) << 1);
		}
	};
}

